	// specific browser shortcuts

	this.isNS4x = (this.isNS && this.versionMajor == 4);

	this.isNS40x = (this.isNS4x && this.versionMinor < 4.5);

	this.isNS47x = (this.isNS4x && this.versionMinor >= 4.7);

